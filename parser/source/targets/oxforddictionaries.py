class OxfordDictionaries(Target):
    MAIN_URL = 'https://en.oxforddictionaries.com/'

    DEFAULT_LIMIT = None

    MIN_DESCRIPTION_LENGTH = 25
    MIN_DESCRIPTION_WORDS = 5

    def __init__(self, image, wiki, logger, keepers, limit):
        super().__init__(image, wiki, logger, keepers, limit)
        self._fetcher = Proxy(logger)

    def _get_items(self):
        items = []
        for category in HARD_CODE_ITEMS:
            for word in category['words']:
                title = f'{word.capitalize()} ({category["subtitle"].capitalize()})'
                url = f'{self.MAIN_URL}definition/{word}'
                items.append({
                    'url': url,
                    'title': title,
                    'category': 'Thing',
                })
        shuffle(items)
        return items[:self._limit]

    def _from_target(self, url, title):
        response = self._dead_fetch(url)
        response = BeautifulSoup(response.content, 'lxml')
        poses = response.find_all('span', {'class': 'pos'})
        gramb = None
        for pos in poses:
            if 'noun' in pos.text.lower():
                gramb = pos.parent.parent
                break

        if gramb is not None:
            ind = gramb \
                .find('div', {'class': 'trg'}) \
                .find('p', recursive=False) \
                .find('span', {'class': 'ind'})
            if ind is not None:
                description = ind.text.strip()
                return {
                    'name': title,
                    'description': description,
                    'link': url,
                    'source': 'oxforddictionaries',
                }
        return None
